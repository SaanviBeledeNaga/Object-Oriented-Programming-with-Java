public class For
{
	public static void main(String[] args)
	//using for statement 28-08-2025
		{
			//post-increment
			for(int i=0;i<5;i++)
			{
				System.out.println(i);
			}
			//pre-increment 
			for(int i=0;i<5;++i)
			{
				System.out.println(i);
			}
			//post-decrement
			for(int i=5;i>0;i--)
			{
				System.out.println(i);
			}
			//pre-decrement
			for(int i=5;i>0;--i)
			{
				System.out.println(i);
			}
		}
}	
